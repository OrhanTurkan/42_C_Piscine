#include <unistd.h>

int	main(int ac, char **arg)
{
	int		i;
	char	k;

	i = 0;
	if (ac > 0)
	{
		while (arg[0][i] != '\0')
		{
			k = arg[0][i];
			write(1, &k, 1);
			i++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
