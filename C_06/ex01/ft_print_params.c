#include <unistd.h>

int	main(int ac, char **arg)
{
	int	i;
	int	j;

	i = 1;
	j = 0;
	if (ac > 1)
	{
		while (i < ac)
		{
			j = 0;
			while (arg[i][j])
			{
				write(1, &arg[i][j], 1);
				j++;
			}
			i++;
			write(1, "\n", 1);
		}
	}
	return (0);
}
